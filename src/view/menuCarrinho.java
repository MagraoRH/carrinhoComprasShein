    public void addRowToTable() {
        DefaultTableModel model = (DefaultTableModel) jtProdutos.getModel();
        model.getDataVector().removeAllElements();//remove todas linhas
        model.fireTableDataChanged();
        Object rowData[] = new Object[4];
        produtoServicos proS = services.servicosFactory.getProdutoServicos();
        for (produto p : proS.listaProduto()) {
            rowData[0] = p.getQuantidade();
            rowData[1] = p.getNome();
            rowData[2] = p.getPreco();
            rowData[3] = p.getIdProduto();
            model.addRow(rowData);
        }
    }

