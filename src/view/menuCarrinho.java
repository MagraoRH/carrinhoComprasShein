    private void jbRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRemoverItemActionPerformed
        int linha;
        int codigoBarras;
        linha = jtProdutos.getSelectedRow();
        codigoBarras = (int) jtProdutos.getValueAt(linha, 3);
        produtoServicos proS = services.servicosFactory.getProdutoServicos();
        Object[] resp = {"Sim", "Não"};
        int resposta = JOptionPane.showOptionDialog(this, "Deseja realmente remover este produto da lista?", "Remove Produto", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, resp, resp[0]);
        if (resposta == 0) {
            proS.deletaProduto(Integer.toString(codigoBarras));
            JOptionPane.showMessageDialog(this, "Produto removido com sucesso!");
            addRowToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Cancelada a opção de remover produto!!");
            addRowToTable();
        }
    }//GEN-LAST:event_jbRemoverItemActionPerformed

